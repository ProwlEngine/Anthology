namespace Prowl.Graphite;

/// <summary>Constant color sampled when address mode is Border.</summary>
public enum SamplerBorderColor : byte
{
    /// <summary>Transparent black (0,0,0,0).</summary>
    TransparentBlack,
    /// <summary>Opaque black (0,0,0,1).</summary>
    OpaqueBlack,
    /// <summary>Opaque white (1,1,1,1).</summary>
    OpaqueWhite,
}
